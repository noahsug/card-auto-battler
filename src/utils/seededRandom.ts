export class Random {
  public state: number;

  static getRandomSeed(seedSeed: number = Math.random()) {
    if (seedSeed < 0 || seedSeed > 1) {
      throw new RangeError(`The seed's seed must be between 0 and 1`);
    }
    return Math.floor(RANDOM_VALUES.length * seedSeed);
  }

  constructor(seed: number = Random.getRandomSeed()) {
    if (!Number.isInteger(seed)) {
      throw new TypeError('seed must be an integer');
    }
    this.state = seed;
  }

  @BoundMethod()
  next(): number {
    const value = RANDOM_VALUES[this.state] / MAX_VALUE;
    this.state = (this.state + 1) % RANDOM_VALUES.length;
    return value;
  }

  @BoundMethod()
  nextInt(min: number, max: number) {
    return Math.floor(this.next() * (max - min + 1)) + min;
  }
}

